//Decimal to Octal 
//Decimal format specifier:%d
//Octal format specifier : %o
#include<stdio.h>
int main()
{
    int deci;
    printf("Enter a Decimal number:");
    scanf("%d",&deci);
    printf("The Octal number is: %o",deci);
    return 0;
}

