// log means ln
// Natural Logarithm
#include <stdio.h>
#include <math.h>
int main()
{
    int x;
    double result;
    printf("Enter the value of x:");
    scanf("%d", &x);
    result = log(x);
    printf("Natural Logarithm of x is=%lf", result);
    return 0;
}




